//Switch statement 

let a = "q";
switch (a) {
    case "a":
        console.log ("A is vowels ")
        break;
    case "e":
        console.log ("E is vowels ")
        break;
    case "i":
        console.log ("I is vowels ")
        break;
    case "o":
        console.log ("O is vowels ")
        break;
    case "u":
        console.log ("U is vowels ")
        break;
    default:
        console.log ("word not vowels ")
        break;
}